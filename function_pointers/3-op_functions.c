#include "3-calc.h"
/**
 * op_add - add two numbers
 * @a: first number
 * @b: second number
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that substract two numbers
 * @a: first number
 * @b: second number
 * Return: difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that multiplies two numbers
 * @a: first number
 * @b: second number
 * Return: product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that divide two numbers
 * @a: first number
 * @b: second number
 * Return: division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - funciton that returns the remainder of the division
 * @a: first number
 * @b: second number
 * Return: remainder of the division
 */

int op_mod(int a, int b)
{
	return (a % b);
}
